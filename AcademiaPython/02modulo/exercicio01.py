#Estrutura de Condicoes
# Operadores Relacionais sao operadores que fazem comparacoes entre esses numeros, baseado neste comparativo e exibido seu resultado  ou algo diferente ao resultado
# Operadores relacionais ( == igual, != diferente, > maiorque, < menorque, >=  maior ou igual, <= menor ou igual)
#play list numero 38

# variael com tipo predefinido esperando dados
idade = int(input('Quantos anos voce tem: '))

#estrutura de condicao baseada nos dados recebidos, a variavel idade esta sendo usada como um objeto para decisao em uma condicao.
if idade >= 18:
    print('Maior de Idade')
else:
    print('Menor de idade')

